NUMBER_A = 10
NUMBER_B = 3

# soma
print(f"Sum: {NUMBER_A} + {NUMBER_B} = ", NUMBER_A + NUMBER_B)
# 15

# subtração
print(f"Sub: {NUMBER_A} - {NUMBER_B} = ", NUMBER_A - NUMBER_B)
# 9

# módulo
print(f"Mod: {NUMBER_A} % {NUMBER_B} = ", NUMBER_A % NUMBER_B)
# 0

# divisão retornando float
print(f"Div returning float: {NUMBER_A} / {NUMBER_B} = ", NUMBER_A / NUMBER_B)
# 4.0

# divisão retornando int
print(f"Div returning int: {NUMBER_A} // {NUMBER_B} = ", NUMBER_A // NUMBER_B)
# 4

# exponencial
print(f"Expo {NUMBER_A} ** {NUMBER_B}: ", NUMBER_A**NUMBER_B)
# 10 elevado a 3: 1000

