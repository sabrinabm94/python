balance = 500
print("Balance:", balance)
#500

#add
deposit = 100
balance += deposit
print(f"Balance += {deposit}:", balance)
#600

#sub
withdrawal = 200
balance -= withdrawal
print(f"Balance -= {withdrawal}:", balance)
#400

#muli
deposit = 2
balance *= deposit
print(f"Balance *= {deposit}:", balance)
#800

#div
withdrawal = 4
balance /= deposit
print(f"Balance /= {withdrawal}:", balance)
#200

#mod
withdrawal = 100
balance %= deposit
print(f"Balance %= {withdrawal}:", balance)
#0

#exp
balance = 100
deposit = 2
balance **= deposit
print(f"Balance **= {deposit}:", balance)
#10000

