# Range
# Usado para obter uma sequência de números inteiros a partir de um int
print(list(range(4)))
# 0,1,2,3

print(list(range(3, 10)))
# começa em 3, termina no 10 (de 3 - 9)
# 3,4,5,6,7,8,9

print(list(range(3, 10, 3)))
# começa em 3 (start), termina no 10 (stop) de 3 - 9, de 3 em 3 (step)
# 3,6,9
